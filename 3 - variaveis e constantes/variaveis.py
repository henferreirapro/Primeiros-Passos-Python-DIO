# BOAS PRATICAS PARA DECLARAR VARIAVEIS.
"""
  podemos declarar uma variavel usando o formato Snake Case,
  uma variavel que usa underline no lugar dos espaços

  Ou apenas o nome da variavel como nome ou idade.

  Exemplo de Snake Case: nome_completo
"""

idade = 28
nome = "Henrique"

print(f"Meu nome é {nome} e tenho {idade} anos.")


# Alterando a variavel
nome = "Mi"

print(f"Meu nome é {nome} e tenho {idade} anos.")
